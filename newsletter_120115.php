                        <h1>Stadtratswahlen</h1>
                        <p>
                        Im kommenden Herbst finden in der Stadt Bern die Stadt- und Gemeinderatswahlen statt.
                        Es ist unser Ziel, das Wahlresultat aus dem Jahr 2008 zu übertreffen, 
                        als wir mit 4 Stadtratssitzen auf Anhieb Fraktionsstärke erreichten. 
                        Obwohl wir hoffen, dass wir unsere Wählerinnen und Wähler mit unserer 
                        Arbeit in den letzten 3 Jahren überzeugen konnten, 
                        brauchen wir zur Erreichung dieses Ziels eine starke und komplette Liste und einen engagierten Wahlkampf. 
                        Einen medialen Auftakt zu den Wahlen hat die BZ übers Jahresende mit den Beiträgen 
                        zu den im Stadtparlament vertretenen Parteien bereits gemacht: 
                        <a href="http://www.bernerzeitung.ch/region/bern/GLP--die-kuehl-berechnenden-Gruenen/story/30959972">
                        Die kühl berechnenden Grünen
                        </a>
                        </p>
                        <p>
                        Damit wir mit einer vollständigen Stadtratsliste antreten können, 
                        benötigen wir 40 Kandidatinnen und Kandidaten. 
                        Dabei ist es unser Ziel, dass die Liste betreffen Alter, Geschlecht und Stadtteile möglichst ausgewogen ist.
                        </p>
                        <p>
                        Wir freuen uns über alle, welche Interesse an einer Kandidatur haben. 
                        Mit einem Mail an <a href="mailto:bern@grunliberale.ch">bern@grunliberale.ch</a> 
                        könnt Ihr Euer Interesse anmelden, gerne beantworten wir natürlich auch allfällige Fragen.
                        </p>
                        <h1>Gemeinderatswahlen </h1>
                        <p>
                        Ende Februar entscheiden die Mitglieder darüber, ob und mit wem die Grünliberalen 
                        auf der Mitte-Liste an den Gemeinderatswahlen teilnehmen werden. 
                        Stadtrat Claude Grosjean hat sein Interesse an einer Kandidatur angemeldet. 
                        Grundsätzlich steht es allen Mitgliedern der Grünliberalen Stadt Bern offen, 
                        sich bis am 31. Januar 2012 beim Präsidenten der Sektion oder unter <a href="mailto:bern@grunliberale.ch">bern@grunliberale.ch</a> 
                        für eine Gemeinderatskandidatur zu bewerben.
                        </p>
